const heroSlides = [...document.querySelectorAll(".hero-slide")];
const languageButtons = [...document.querySelectorAll(".language-btn")];
const translatableNodes = [...document.querySelectorAll("[data-i18n]")];
const navToggle = document.querySelector(".nav-toggle");
const navMenu = document.querySelector(".nav-menu");
const scrollTopButton = document.querySelector(".scroll-top");
const whatsappButton = document.querySelector(".floating-whatsapp");
const lightbox = document.querySelector(".lightbox");
const lightboxImage = lightbox.querySelector("img");
const lightboxCaption = lightbox.querySelector("p");
const lightboxClose = lightbox.querySelector(".lightbox-close");
const portfolioButtons = [...document.querySelectorAll(".portfolio-item")];
const socialPlaceholders = [...document.querySelectorAll("[data-social-placeholder]")];
const metaDescription = document.querySelector('meta[name="description"]');
const ogTitle = document.querySelector('meta[property="og:title"]');
const ogDescription = document.querySelector('meta[property="og:description"]');

let currentLanguage = "en";
let currentSlide = 0;

function updateLanguage(lang) {
  const dictionary = translations[lang];
  currentLanguage = lang;

  document.documentElement.lang = lang;
  document.title = dictionary.metaTitle;
  metaDescription.setAttribute("content", dictionary.metaDescription);
  ogTitle.setAttribute("content", dictionary.ogTitle);
  ogDescription.setAttribute("content", dictionary.ogDescription);

  translatableNodes.forEach((node) => {
    const key = node.dataset.i18n;
    if (dictionary[key]) {
      node.textContent = dictionary[key];
    }
  });

  languageButtons.forEach((button) => {
    button.classList.toggle("is-active", button.dataset.lang === lang);
  });
}

function rotateSlides() {
  heroSlides[currentSlide].classList.remove("is-active");
  currentSlide = (currentSlide + 1) % heroSlides.length;
  heroSlides[currentSlide].classList.add("is-active");
}

function handleScrollState() {
  scrollTopButton.classList.toggle("is-visible", window.scrollY > 420);
}

function openLightbox(button) {
  const captionKey = button.dataset.captionKey;
  lightboxImage.src = button.dataset.full;
  lightboxImage.alt = button.querySelector("img").alt;
  lightboxCaption.textContent = translations[currentLanguage][captionKey];
  lightbox.showModal();
  document.body.style.overflow = "hidden";
}

function closeLightbox() {
  if (!lightbox.open) return;
  lightbox.close();
  document.body.style.overflow = "";
}

function setupRevealAnimations() {
  const revealTargets = document.querySelectorAll(
    ".section-heading, .service-card, .review-card, .portfolio-item, .contact-card, .about-points article, .video-card, .trust-grid div"
  );

  revealTargets.forEach((node) => {
    node.setAttribute("data-reveal", "");
  });

  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("is-visible");
          observer.unobserve(entry.target);
        }
      });
    },
    { threshold: 0.16 }
  );

  revealTargets.forEach((node) => observer.observe(node));
}

languageButtons.forEach((button) => {
  button.addEventListener("click", () => updateLanguage(button.dataset.lang));
});

navToggle.addEventListener("click", () => {
  const expanded = navToggle.getAttribute("aria-expanded") === "true";
  navToggle.setAttribute("aria-expanded", String(!expanded));
  navMenu.classList.toggle("is-open");
});

document.querySelectorAll(".nav-menu a").forEach((link) => {
  link.addEventListener("click", () => {
    navMenu.classList.remove("is-open");
    navToggle.setAttribute("aria-expanded", "false");
  });
});

scrollTopButton.addEventListener("click", () => {
  window.scrollTo({ top: 0, behavior: "smooth" });
});

whatsappButton.addEventListener("click", () => {
  window.open("https://wa.me/18592133957", "_blank", "noopener,noreferrer");
});

portfolioButtons.forEach((button) => {
  button.addEventListener("click", () => openLightbox(button));
});

lightboxClose.addEventListener("click", closeLightbox);
lightbox.addEventListener("click", (event) => {
  const box = lightbox.getBoundingClientRect();
  const clickedOutside =
    event.clientX < box.left ||
    event.clientX > box.right ||
    event.clientY < box.top ||
    event.clientY > box.bottom;

  if (clickedOutside) {
    closeLightbox();
  }
});

document.addEventListener("keydown", (event) => {
  if (event.key === "Escape") {
    closeLightbox();
  }
});

socialPlaceholders.forEach((link) => {
  link.addEventListener("click", (event) => {
    event.preventDefault();
    window.alert(translations[currentLanguage].socialPlaceholderAlert);
  });
});

window.addEventListener("scroll", handleScrollState, { passive: true });

updateLanguage("en");
handleScrollState();
setupRevealAnimations();
setInterval(rotateSlides, 5200);
